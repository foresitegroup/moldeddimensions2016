<h1>Project/Quality Engineer International Division</h1>

<h2>REPORTS TO: <span style="color: #343435;">Global Business Manager</span></h2>
<br>

<h2>POSITION PURPOSE</h2>
To assure all International First Article samples meet customer quality and profitability objectives. To coordinate all communication on First Article samplings and provide regular updates to the customer and within Molded Dimensions.  To assure all International products meet customer specifications while achieving the company's other performance objectives.<br>
<br>

<h2>PRINCIPLE DUTIES</h2>
<ol>
  <li>
    Manages projects from receipt of customer purchase order, through the First Article process and customer approval, until first production PO.
    <ol type="a">
      <li>Communicates regularly with the customer: initially to understand part function and operating conditions, make recommendations, and set expectations; weekly with status updates; and as needed to clarify and resolve issues.</li>
      <li>Keeps Sales, Global Business Manager, and others informed of project status.</li>
      <li>Works with the Chemist to coordinate testing of and recording results for test materials and samples.</li>
      <li>Works with the supplier to develop a process to reliably meet customer quality requirements with optimum efficiency. Ensures that process is followed during the First Article run. Works with supplier during sampling to debug processes and tools. Communicates part function and quality expectations to International team.</li>
      <li>Anticipates risks and failure modes, working proactively to prevent them.</li>
      <li>Selects inspection methods. Measures parts utilizing a variety of equipment, from a durometer gage to the vision system. Visually inspects parts. Performs any additional testing required.</li>
      <li>Performs first article inspections as prioritized by the Global Business Manager.</li>
      <li>Prepares ISIRs, approval forms, and all customer-requested documentation. May work directly with customers to ensure their expectations are understood and met.</li>
      <li>Consults with the Global Business Manager and team on any non-conformances.</li>
      <li>Promptly communicates inspection results, approvals, and customer feedback.</li>
      <li>Establishes quality requirements with the suppliers to ensure production parts have all the desired characteristics of First Article samples.</li>
      <li>Meets promised delivery dates.</li>
      <li>Analyzes First Article results. Recommends and implements changes to ensure all equipment, molds, materials and methods are production-ready upon customer approval.</li>
      <li>Ensures all changes from original written contracts/instructions/drawings are documented and communicated to those affected.</li>
      <li>Controls costs. Alerts Sales and Global Business Manager if tooling or purchased costs deviate significantly from the original quote/estimate.</li>
      <li>Follows ISO procedures.</li>
    </ol>
  </li>
  <li>Works closely with the International team to determine the necessary inspection criteria, and then create, update and implement incoming and supplier inspection sheets, to assure production products continue to meet the requirements.</li>
  <li>Works closely with International Team, customers and suppliers to implement continuous improvement projects in an effort to: reduce manufacturing costs; improve product quality; and/or ensure employee safety.</li>
  <li>Works with chemist to perform and record material test results, to assure material compliance to the known specifications for production products.</li>
  <li>Performs other duties as assigned.</li>
</ol>
<br>

<h2>QUALIFICATIONS</h2>
<ol>
  <li>Bachelor's degree in Engineering</li>
  <li>Two (2) years' experience successfully managing multiple projects in a manufacturing environment with direct customer contact.  Experience transitioning projects from engineering development into manufacturing production.</li>
  <li>Supervisory experience a plus</li>
  <li>Proficiency in AIAG APQP process with a working knowledge of PPAP documentation, SPC, control plans, gage R&R studies, and other common manufacturing quality tools</li>
  <li>ISO 9001:2015 auditing experience a plus</li>
  <li>Understands part drawings with knowledge of measurement methods, including geometric dimensioning and tolerance (GD&T), and statistical techniques</li>
  <li>Strong verbal and written communication skills</li>
  <li>Proficiency in SolidWorks and MS Excel</li>
  <li>Willingness to travel overseas to assist in the development of International supply base</li>
  <li>Proficiency in reading blueprints, determining appropriate inspection techniques, and using measuring equipment, including a vision system</li>
  <li>Fluent in Mandarin a plus</li>
</ol>

