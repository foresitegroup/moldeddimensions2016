Project Management is a priority for Molded Dimensions, Inc. and contributes to our success. We consider ourselves "best in class" for project management and communication.<br>
<br>

<h2>First step, a Project Manager.</h2>
The process starts when the new tooling order or project is kicked off. A dedicated engineering Project Manager (PM) is assigned. The Engineer becomes the hub of project management and communication.<br>
<br>

<h2>Design review.</h2>
At the cross-functional design review, the Sales Engineer provides all project information obtained during the project review and RFQ process including application information and critical features. As part of the design for manufacturability review, the print is audited, tooling design is proposed, the prospective manufacturing routing is considered, material requirements reviewed, sampling and inspection requirements discussed, along with other pertinent project information.<br>
<br>

<h2>PM management and communication.</h2>
<img class="one-half-right" src="images/project-man1.jpg" alt="The project manager oversees rubber injection molding.">
Throughout the remaining process, the PM manages the tooling, material procurement and development, fixtures, scheduling, process development, finishing operations, final inspection, packaging and shipment. During these steps, we find it is critical to keep our customer's purchasing, engineering and quality team informed with weekly PM updates.<br>
<br>

<h2>Final step is post part approval meeting.</h2>
A cross-departmental meeting, reviewing the production, finishing, and inspection processes, ensures the part's successful launch to production.<br>
<br>

The key to this entire process is a knowledgeable PM who oversees the project and becomes the center of the project.

