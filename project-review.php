<h2>Part Design Analysis</h2>
When reviewing a new project, our Sales Engineers analyze and offer part geometry design suggestions to provide an effective, robust part that can be efficiently produced. Tooling design is reviewed to ensure the following:
<ul>
  <li>efficient part removal</li>
  <li>increased part quality</li>
  <li>decreased mold cycle time</li>
  <li>increased tool life</li>
</ul>

Optimizing tooling cavitation is also a priority in order to produce the maximum number of parts in the shortest total cycle.<br>
<br>

<h2>Critical Features Review</h2>
Knowing how a part is used allows our Sales Engineers to review and suggest DFM concepts and address the part's critical features. As part of the process, potential suggested improvements include:
<ul>
  <li>reduced tooling cost</li>
  <li>reduced part wear</li>
  <li>increased part life</li>
  <li>eliminating unnecessary, non-value added features</li>
  <li>reduced material</li>
</ul>

Customer expectations including parting line location, flash extension, critical surfaces, texture and color options are discussed.<br>
<br>

<h2>Material Selection</h2>
Sales Engineers choose from Molded Dimensions, LLC's standard cast polyurethane and rubber formulas and utilize our <a href="materials-engineering.php">Materials Engineer</a> to formulate custom materials. Our material selection process includes reviewing:
<ul>
  <li>Temperature and service environment</li>
  <li>Specific physical property requirements and potential failure modes</li>
  <li>Special material certification requirements and elastomer cost</li>
</ul>
<br>

<h2>Tolerance Review</h2>
Part tolerancing drives the required process operations used to manufacture a part. A review of required tolerances eliminates unnecessary, secondary operations for the most cost effective process routing.<br>
<br>

The Sales Engineering work is the first step to providing a high quality, cost effective part for the life of the product.

