<h1>DIMENSIONAL TOLERANCING</h1>
<h1>FACTORS AFFECTING TOLERANCES</h1>
<h2>Introduction</h2>
The purpose of this section is to list some of the factors affecting tolerances. In general, the degree of reproducibility of dimensions depends upon the type of tooling and rubber used, and the state of the art.<br>
<br>

<h2>Discussion of Factors Affecting Tolerances</h2>
There are many factors involved in the manufacturing of molded rubber products which affect tolerances. Since these may be peculiar to the rubber industry, they are listed here.<br>
<br>

<h2>Shrinkage</h2>
Shrinkage is defined as the difference between corresponding linear dimensions of the mold and of the molded part, both measurements being made at room temperature. All rubber materials exhibit some amount of shrinkage after molding when the part cools. However, shrinkage of the compound is also a variable in itself and is affected by such things as material specification, cure time, temperature, pressure, inserts, and post cure. The mold designer and the compounded must determine the amount of shrinkage for the selected compound and incorporate this allowance into the mold cavity size. Even though the mold is built to anticipate shrinkage, there remains an inherent variability which must be covered by adequate dimensional tolerance. Shrinkage of rubber is a volume effect. Complex shapes in the molded product or the presence of inserts may restrict the lineal shrinkage in one direction and increase it in another. The skill of the rubber manufacturer is always aimed at minimizing these variables, but they cannot be eliminated entirely.<br>
<br>

<h2>Mold Design</h2>
Molds can be designed and built to varying degrees of precision, but not at the same cost. With any type of mold, the mold builder must have some tolerance, and therefore, each cavity will have some variance from the others. Dimensional tolerances on the product must include allowances for this fact. The accuracy of the mold register must also be considered. This is the matching of the various plates of the mold that form the mold cavity. Register is usually controlled by dowel pins and bushings or by self-registering cavities. For molds requiring high precision in dimensions and register, the design work and machining must be more precise and the cost of the molds will be greater than one with commercial requirements.<br>
<br>

<h2>Trim and Finish</h2>
The objectives of trimming and finishing operations are to remove rubber material-such as flash, which is not a part of the finished product. Often this is possible without affecting important dimensions, but in other instances, some material is removed from the part itself. Where thin lips or projections occur at a mold parting line, mechanical trimming may actually control the finished dimension.<br>
<br>

<h2>Inserts</h2>
Most insert materials (metal, plastic, fabric, etc) have their own standard tolerances. When designing inserts for molding to rubber, other factors must be considered, such as fit in the mold cavities, location of the inserts with respect to other dimensions, proper hole spacing to match with mold pins, and the fact that inserts at room temperature must fit into a heated mold. In these matters, the rubber manufacturer can be of service in advising on design features.<br>
<br>

<h2>Distortion</h2>
Because rubber is flexible material, its shape can be affected by temperature. Distortion can occur when the part is removed from the mold or when it is packed for shipment. This distortion makes it difficult to measure the parts properly. Some of the distortion can be minimized by storing the part as unstressed ad possible for 24 hours at room temperature. Some rubber will crystallize (stiffen) when stored at low temperature and must be heated to above room temperature to overcome this condition.<br>
<br>

<h2>Environmental Storage Conditions</h2>
<strong>Temperature:</strong> Rubber, like other materials, changes in dimension with changes in temperature. Compared to other material the coefficient of expansion of rubber is high. To have agreement in the measurement of products that are critical or precise in dimension, it is necessary to specify a temperature at which the parts are to be measured and the time required to stabilize the part at that temperature.<br>
<br>

<strong>Humidity:</strong> Some rubber materials absorb moisture. Hence the dimensions are affected by the amount of moisture in the product. For those products which have this property, additional tolerance must be provided in the dimensions. The effect may be minimized by stabilizing the product in an area of controlled humidity and temperature for a period not less than 24 hours.<br>
<br>

<h2>Dimension Terminology</h2>
The following will provide a common terminology for use in discussing dimensions of molded rubber products, and for distinguishing various tolerance groupings:<br>
<br>

<strong>Fixed Dimension:</strong> Dimensions not affected by flash thickness variation. (Mold Closure) See Figure 1.<br>
<br>

<strong>Closure Dimensions:</strong> Dimension affected by flash thickness variation. (Mold Closure) See Figure 1.<br>
<br>

<img class="centered-image" src="images/standfigure1.gif" alt=""><br>
<br>

In addition to the shrinkage, mold maker's tolerance, trim and finish, a number of other factors affect closure dimensions. Among these are flow characteristics of the raw stock, weight, shape of perform and molding process.<br>
<br>

While closure dimensions are affected by flash thickness variation, they are not necessarily related to basic flash thickness. If a manufacturer plans to machine or die trim a product, the mold will have a built-in flash, which will be thicker than if hand deflashing or tumble trim were to be employed. Thus products purchased from two sources could have different basic flash thickness at the parting line and yet meet drawing dimensions.<br>
<br>

There is usually a logical place for the mold designer to locate the parting line for best dimensional control and part removal. If the product design limits this location, an alternate mold construction will be required, which may affect the tolerance control on the product, and may, in some cases, increase the cost of the mold.<br>
<br>

Registration Dimension: Dimensions affected by the matching of the various plates of the mold that form the mold cavity. Register is usually controlled by dowel pins and bushings or by self-registering cavities.<br>
<br>

There are four levels of dimensional tolerances that are used for molded products.<br>
"A1" High Precision<br>
"A2" Precision<br>
"A3" Commercial<br>
"A4" Basic<br>
<br>

The level selected should be based upon the need with the following guidelines.<br>
<br>

"A1" is the tightest tolerance classification and indicates a high precision rubber product. Such products require expensive molds, fewer cavities per mold, costly in-process controls and inspection procedures. It is desirable that the exact method of measurement be agreed upon between rubber manufacturuer and customer, as errors in measurement may be large in relation to the tolerance. Some materials, particularly those requiring post curing, do not lend themselves to Drawing Designation "A1" tolerances.<br>
<br>

"A2" tolerances indicate a precision product. Molds must be precision machined and kept in good repair. While measurement methods may be simpler than the Drawing Designation "A", careful inspection will usually be required.<br>
<br>

"A3" tolerances indicate a "commercial" product and will normally be used for most products.<br>
<br>

"A4" tolerances apply to products where some dimensional control is required but is secondary to cost.<br>
<br>

When applying tolerances the following rules should be kept in mind.<br>
<ol type="1">
  <li>Fixed dimension tolerance apply individually to each fixed dimension by its own size.</li>
  <li>Closure dimension tolerances are determined by the largest closure dimension and this single tolerance is used for all other closure dimensions.</li>
  <li>Fixed and closure dimensions for a given table do not necessarily go together, and can be split between tables.</li>
  <li>Tolerances not shown should be determined in consultation with the rubber manufacturer.</li>
  <li>Care should be taken in applying standard tolerances to products having wide sectional variations.</li>
</ol>
