<h2>Measurement of Dimensions</h2>
<strong>Conditioning of Parts:</strong> Measurements of dimensions shall be made on parts conditioned at least 24 hours after molding operation. Measurements shall be completed within 60 days after shipment or before the part is put into use, whichever is the shorter time. Care shall be taken to insure that the parts are not subjected to adverse storage conditions.<br>
<br>

In the case of referee measurement, particularly on Drawing Designation "A1" tolerances or for materials known to be sensitive to variations in temperature or relative humidity, the parts in question should be conditioned for a minimum of 24 hours at 23&deg; &plusmn; 2&deg; C (73.4&deg; &plusmn; 3.6&deg; F) and at 50% &plusmn; 5% relative humidity.<br>
<br>

<strong>Methods of Measurements:</strong> Depending upon the characteristics of the dimension to be measured, one or more of the following methods of measurement may be used.<br>
<ol type="a">
  <li>A dial micrometer with a plunger size and loading as agreed upon by the customer and the rubber manufacturer.</li>
  <li>A suitable optical measuring device.</li>
  <li>Fixed gauges appropriate to the dimension being measured.</li>
  <li>Other methods agreed on between customer and supplier.</li>
</ol>
<br>

Under no circumstances should the part be distorted during measurement. On dimensions which are difficult to measure or which have unusually close tolerances, the exact method of measurement should be agreed upon in advance by the rubber manufacturer and the customer.<br>
<br>

<h2>Relative Dimensions</h2>
<strong>General Information:</strong> Relative dimensions such as concentricity, squareness, flatness, parallelism, or location of one or more inserts in the product are dimensions described in relation to some other dimension. Since it is impossible to foresee the many potential designs of all molded products in which relative dimensions will be specified, it is impractical to assign standard drawing tolerance designations to these dimensions. The design engineer should recognize that the more precise the requirement, the more expensive the product. He must allow the rubber manufacturer to use support pins, lugs, chaplet pins, or ledges in the mold to provide positive location and registration of the insert or inserts in the mold cavity. With this in mind, it is suggested that the design engineer discuss relative dimensional tolerances on all products directly with the rubber manufacturer.<br>
<br>

Other factors do affect tolerances to some minor degree. Our attempt has been to acquaint the design engineer with background information on the major factors which result in the need for tolerances on molded rubber products.<br>
<br>

<strong>Examples of Relative Dimensions:</strong> Several examples of relative dimensions the design engineer may be required to consider are shown:<br>
<ol type="A">
  <li>Concentricity</li>
  <li>Squareness</li>
  <li>Flatness</li>
  <li>Parallelism</li>
</ol>
<br>

In all cases the tolerances should be considered only as a very general guide.

