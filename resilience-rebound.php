The reliance of urethane increases as temperature is increased from 0 to 50F (-8C to 10C) and then becomes almost constant. Being almost constant permits more confidence in design where service temperature may vary considerably.<br>
<br>

Heat build-up in urethane parts, under high frequency flexing, exceeds that of conventional elastomers and is the usual cause of premature failure under dynamic conditions. Because of the low thermal conductivity of urethane elastomers, heat developed by internal friction cannot readily be dissipated. The effect of heat build-up is, therefore, a very important consideration when designing with urethanes. Its adverse effects can be minimized by using thin cross- sections from which heat is more easily dissipated. The high strength and load bearing capacity of urethane elastomers makes possible the use of sections, which are thin enough to dissipate heat at the same rate at which it is developed.<br>
