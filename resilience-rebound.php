Resilience is defined as the ratio of energy given up in recovery from deformation to the energy required to produce the deformation, usually expressed in percent. Hysteresis is the percent energy loss per cycle of deformation of 100% minus the resilience percentage. Hysteresis is the result of internal friction and is the conversion of mechanical energy into heat. Heat build-up is measured as the temperature rise resulting from hysteresis.<br>
<br>

In general, resilience is determined in one of four ways - from a low speed stress-strain loop, by impact tests, by free vibration or forced vibration methods.<br>
<br>

Low speed stress - strain is obtained by loading and unloading a specimen in tension, compression or shear using a low rate of strain and large deformation. Since most practical applications involve vibratory stresses of relatively high frequency and low amplitude, the low-speed stress-strain loop is not often used for measuring hysteresis.<br>
<br>

The most widely used methods for measuring resilience by impact involve rebound in some form. A very simple test consists of dropping a metal ball from a known height onto a firmly supported rubber specimen and measuring the height of rebound, as with the Bashore Resiliometer.<br>
<br>

An impact test, however, is not equal to a vibration test since there is no cyclic interchange of potential and kinetic energy. A widely used instrument that measures vibratory resilience is the Yerzley Oscillograph. This instrument is popular because it involves a relatively high speed deformation (many times faster than a stress-strain loop, although considerably slower than with impact resilience tests) through one or more complete vibration cycles and yields precise and reproducible data. However, the frequency is not the same order of magnitude as that of many applications involving vibration.<br>
