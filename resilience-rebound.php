The apparatus consists of a balanced beam supported on knife edges, with weights which are added to one end to strain the specimen on the opposite side of the knife edges. When the weights are released, a trace of damping curve is automatically recorded. No significant test values can be obtained on materials which have a moduli greater than 280 psi in compression with 10% deformation. Yerzley tests are, therefore, limited to urethane (durometer 90A or below).<br>
<br>

The Bashore rebound test can be used on rubber of all hardnesses, but does not yield results which are precise and distinguishing as Yerzley resilience. Impact may cause a rise in temperature resulting from heat generated within the specimen. Resilience is a function of temperature and usually increases when rubber is heated.<br>
<br>

Forced Vibration methods may be used to measure resilience, but they are usually employed to determine heat build-up in the specimen. Three flexometers described in ASTM D-223 are most commonly used for this measurement. These are known as Goodyear, Firestone and St Joe Flexometer. They are most frequently used to compare various compositions with one whose performance has been determined by actual use.<br>
<br>

There is a tendency to assume that a composition having high hysteresis will be unsatisfactory for almost any use. This is not unnecessarily true. In certain vibration damping applications, compounds having relatively low resilience may be desirable because their damping effect limits the maximum amplitude which may develop in service.<br>
<br>

For vibration damping purposes resilience requirements are determined largely by the frequency and amplitude of vibration. Hysteresis in a low resilience compound would cause excessive heat build-up in the part. In this case a highly resilient composition should be used.<br>
<br>

Damping refers to the reduction of vibration amplitude in a free vibration system. Damping is a result of hysteresis and the two terms are frequently used interchangeably.<br>
<br>

Heat generation measured by the temperature rise, or the equilibrium temperature, for a sample under forced vibration at non-resonance is more nearly related to the requirements of actual service than is resilience. The temperature rise at a given amplitude depends upon both the resilience and the compression/deflection of the rubber compound. The resilience determines the proportion of the vibrational energy which is converted into heat, but the actual value of the vibrational energy at a given amplitude is proportional to the dynamic modulus.<br>
<br>

<h2>Effect of Amplitude and of Frequency on Vibration Properties</h2>
If there is no appreciable rise in the temperature of the rubber, the dynamic modulus and dynamic resilience are independent of frequency for the ordinary range of mechanical frequencies. Any rise in temperature of the rubber due to internal heat generation with increase with frequency, tending to lower the dynamic modulus and raise the resilience. The dynamic properties of gum compounds are usually not affected by amplitude; but with filled compounds, the dynamic modulus decreases with the increase in amplitude even if the temperature in the rubber is constant. Any rise in temperature contributes to this effect. Resilience is not affected by the amplitude expect indirectly by temperature changes.<br>
<br>

