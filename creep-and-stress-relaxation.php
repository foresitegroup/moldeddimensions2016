<h1>Creep and Stress Relaxation</h1>
<h2>Creep</h2>
When subjected to load, all elastomers exhibit an increasing deformation with time, know as creep. This occurs at any stress level and takes place in compression, tension and shear loadings and varies for each type of loading. In service, creep can be minimized by using low working stresses and avoiding high temperatures. No rapid method has been developed for its measurement because there is no know way of accelerating time effects without introducing inaccuracies in predicting rate of creep.<br>
<br>

Creep is usually expressed in percent of deformation after the part is loaded rather than the unloaded dimension. Determination of creep takes place after some arbitrary short time interval such as one minute, five minutes or even on day after applying the load. Creep, expressed as a percent, equals total deformation minus initial deformation divided by initial deformation, time 100. In the initial stage, creep occurs at a relatively high rate and then continues at a very slow rate. Failure can occur after an extended period of high stress. Figure 1 illustrates characteristic creep curves. AB in the high stress creep curve indicates the failure phase where actual fracture can occur.<br>
