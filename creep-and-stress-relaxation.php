<h2>Stress Relaxation</h2>
Stress relaxation is the loss in stress when it is held at a constant strain over a period of time. It is usually expressed in terms of percent stress remaining after an arbitrary length of time at a given temperature.<br>
<br>

There is no standard method for determining stress relaxation. However, many laboratories have developed relaxation cells. These cells utilize the compression set specimen and the test procedure parallels ASTM D-395 Method B. Stress relaxation for 50A urethane is shown in Figure 3.<br>
