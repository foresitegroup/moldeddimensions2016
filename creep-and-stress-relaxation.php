Below the failure zone, when stress is removed, the part will attempt to return to its original dimension; however, it will never fully recover. The unrecoverable portion is called permanent set. Loads which allow intermittent recovery will exhibit less creep than if continuously loaded. However, continuous vibratory loading will increase creep since internal heat is generated.<br>
<br>

Strain relaxation is important in applications such as engine mountings since it influences the alignment of various parts of the equipment. Yet, it is difficult to predict these properties for a given application without resorting to simulated service tests because several factors have an important effect on them. Chief among these are amount of strain, operating temperature and changes in these two resulting from vibration.<br>
<br>

The relative effects of variables have not yet been correlated so that results of tests under one set of conditions will permit accurate prediction of creep under another set of conditions. It has been established that the higher the initial strain, the higher the creep; also, the higher the temperature, the higher the creep. In general, the degree of creep is dependent on the type of strain. Creep is greater under tension strain then under equal compression strain. Creep is also increased more under dynamic loading then under static loading.<br>
<br>

The creep characteristics of two urethane polymers, over a ten-month period, are shown on Figure 2. After approximately 3000 hours (18 weeks) creep reaches a plateau and becomes almost constant. The amount of creep is a function of stress level. This involves a stress of 400 psi. Creep will continue at a very low rate after this point, which is the classic behavior of elastomers.<br>
