<h1>Electrical Properties</h1>
The electrical properties of elastomers most commonly measured are as follows:<br>
<ol type="1">
  <li>Resistivity</li>
  <li>Dielectric Strength</li>
  <li>Dielectric Constant</li>
  <li>Power Factor</li>
</ol>
<br>

Materials suitable for electrical insulators have high dielectric strength and resistivity and low dielectric constant and power factor. Such a combination is rarely obtained since companion physical and chemical properties are also required. There is no single best overall material, but urethane offers a favorable combination of properties.<br>
<br>

<h2>Direct Current Resistivity</h2>
Direct current resistivity, or volume resistivity, is similar to insulation resistance in that both indicate the magnitude of electrical current expected to pass through the sample located between two electrodes. This test may be used to check the suitability of an elastomeric composition for electrical insulation.<br>
<br>

Resistivity or specific resistance to electric current is usually considered to be a DC property and is measured as such. It can be defined as the resistance between opposite faces of a unit cube where precautions have been taken to allow no current flow along the other four faces of the cube.<br>
<br>

<h2>Conductivity</h2>
Elastomeric compositions are considered electrically conductive when they possess a direct current resistivity of less than 10^5 to 10^7 ohm-cm. Conductive compounds find their principal application where the dissipation of static electricity is desired.<br>
<br>

<h2>Dielectric Strength</h2>
The dielectric strength of an elastomer is the voltage required to puncture, a sample of known thickness and is expressed as volts per mil of thickness. The rate of voltage application, the geometry of the electrodes, and the shape of the test specimen influence the results obtained.<br>
<br>

<h2>Dielectric Constant and Power Factor</h2>
The dielectric constant, or specific inductive capacity (SIC), is a measure of an insulator's ability to store electrical energy. The dielectric constant is the ratio of the electrical capacity of a condenser using the elastomer under test as the dielectric, to the capacity of a similar condenser using air as the dielectric. One of the factors which influences the dielectric constant is frequency, and tests may be run from a low of 25 cycles per second to many megacycles per second.<br>
<br>

The power factor of an electrical insulation material indicates its tendency to generate heat in service. If a capacitor using an elastomer as the dielectric is charged by a direct current and then immediately discharged, there is an energy loss in the form of heat. If this capacitor is repeatedly charged and discharged by an alternating current, the electrical loss results in heating. The ratio of this loss to the energy required to charge the capacitor is known as power factor. It is expressed as a decimal fraction or a percent of the charging energy.<br>
<br>

