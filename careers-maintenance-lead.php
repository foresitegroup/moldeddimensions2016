<h1>Maintenance Lead</h1>

To maximize equipment up-time through execution of a solid preventive maintenance program and minimize equipment downtime through timely repair of any issues experienced by the manufacturing group.  This position is a "working" lead, meaning that the successful candidate will be responsible for both making repairs himself/herself and managing the rest of the maintenance staff.<br>
<br>

<strong>Principle Duties</strong><br>
<ol>
  <li>Perform general maintenance work as required.</li>
  <li>Work closely with Manufacturing to maximize machine availability.</li>
  <li>Manage the maintenance staff.  Assign work orders and projects as necessary.  Provide priority clarity and expectations to direct reports.</li>
  <li>Participate in and/or support the Safety Committee with findings from safety audits.  Manage the Lock-Out/Tag-Out program.  Interface with Safety/Environmental consultant.</li>
  <li>Manage suppliers to perform contract repairs and establish a source of supply for needed spare parts.</li>
  <li>Manage the preventive maintenance program, including adding new pieces of equipment to the program.  Responsible for timely completion of all PM activities.</li>
  <li>Participate in the process for purchase of capital equipment in conjunction with the Director of Operations.</li>
  <li>Identify training needs of the maintenance group and take responsibility for carrying out or arranging training.</li>
  <li>Provide performance feedback to direct reports.  Prepare and deliver annual reviews and handle any disciplinary situations that may arise.  Discuss pay rate changes with the Director of Operations.</li>
  <li>Maintain the working condition of all maintenance equipment.  Recommend and purchase new maintenance equipment as needed by the business.</li>
  <li>Treat coworkers with respect, contribute to a positive and productive atmosphere, and challenge self and others to continuously improve.</li>
  <li>Maintain a clean and safe work area.</li>
  <li>Perform other duties as assigned.</li>
</ol>
<br>

<strong>Qualifications</strong><br>
<ol>
  <li>Expertise in general maintenance functions including, but not limited to, electrical, pneumatic, hydraulic, welding, fabrication, PLC's, reading prints and schematics.</li>
  <li>Experience managing contractors/vendors and purchasing supplies.</li>
  <li>5-10 years of experience in a manufacturing environment.  Previous supervisory experience is preferred.</li>
  <li>Basic computer knowledge -- e-mail, Excel, internet, etc.</li>
  <li>Ability to continuously communicate effectively with manufacturing to prioritize repairs and deliver timeline for completion.</li>
</ol>
