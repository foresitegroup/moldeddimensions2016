<h1>Vice President of Operations</h1>

<strong>SUMMARY<br>
The Vice President of Operations will plan, direct, coordinate, and oversee operations activities in the organization, ensuring development and implementation of efficient operations and cost-effective systems to meet current and future needs of the organization.</strong><br>
<br>

<strong>ESSENTIAL DUTIES AND RESPONSIBILITIES</strong> include the following:<br>
Responsible for the direct and indirect supervision of a 3-shift, multi-location, 50-plus person operation<br>
<br>

Plan, direct and oversee the entire operations process, including preventative maintenance to achieve and exceed production, quality, safety and profitability goals and objectives.<br>
<br>

Develop policies, processes and procedures that will support organic growth.<br>
<br>

Apply Lean Manufacturing initiatives to improve operating efficiency, control labor costs, reduce rework and scrap, and deliver quality products on time.<br>
<br>

Hire, train, coach and mentor business unit managers as well as all production and maintenance workers.<br>
<br>

Participate as a member of the company leadership team to help develop and execute the overall business strategy.<br>
<br>

<strong>SUPERVISORY RESPONSIBILITIES</strong><br>
<ul>
  <li>5-10 direct; 50+ indirect reports</li>
  <li>This position leads managers in the respective business units</li>
</ul>
<br>

<strong>QUALIFICATIONS</strong><br>
<ul>
  <li>Minimum of 10 years manufacturing experience with at least 5 years in a management capacity; Experience in rubber molding is preferred; injection or other molding experience desirable.</li>
  <li>Experience operating a P&L</li>
  <li>Experience with overseas sourcing a plus</li>
  <li>Experience with machining centers / tooling a plus</li>
  <li>Strong track record of implementing continuous improvement practices in operations using lean manufacturing, 5S, Kaizen and Six Sigma tools and methods.</li>
  <li>Experience managing teams to high performance standards.</li>
  <li>Experience with ERP/MRP implementations / execution a plus</li>
  <li>Experience transforming a legacy organization a plus</li>
  <li>Excellent analytical and reasoning skills, plus excellent organization skills required</li>
</ul>
<br>

<strong>EDUCATION and/or EXPERIENCE</strong><br>
Bachelor's degree in engineering or other related discipline is preferred, but not required.<br>
