<h1>Resin Prep Floater</h1>

Job involves loading and unloading of molds following written procedures, operating hydraulic presses, and troubleshooting quality defects while making appropriate adjustments.<br>
<br>

Responsibilities include interpreting formula sheets and accurately measuring and dosing resin and material additives into properly sized buckets.  Ensuring molders have material ready and heated for every job on the production schedule.  Monitoring material inventory and alerting the production scheduler/buyer if inventory levels are low.  Ensuring resin barrels are on heat long enough to allow for dosing but not too long to where the material goes bad.  Keeping the work area very clean and organized.<br>
<br>

Ideal candidates would have great multi-tasking skills, will be detail-oriented, and must be able to read and follow process instructions and formula sheets.  Basic math skills and ability to accurately use measurement tools and equipment is necessary.  Attention to detail and strong communication skills required.  Requires the ability to lift 50 lbs. regularly.  Manufacturing experience a plus, but not necessary if candidates can learn quickly and work in a fast-paced environment.<br>
