<h1>MATERIAL SELECTION PROCESS</h1>
At Molded Dimensions, we know it's not always easy to pick an elastomer. There are many factors to consider. To assist our customers, our Engineers have developed a simple 9 step process to help you make an elastomer selection based on your custom application and specific requirements.<br>
<br>

<ol type="1" style="font-style: italic;">
  <li>
    Temperature information
    <ul style="font-style: normal; list-style-type: disc;">
      <li>Service Temperature Range (minimum, maximum)</li>
    </ul>
  </li>
  <li>
    Service Environment
    <ul style="font-style: normal; list-style-type: disc;">
      <li>Ozone</li>
      <li>Water</li>
      <li>Oxygen</li>
      <li>Special Fluids</li>
      <li>Outdoor Weathering</li>
    </ul>
  </li>
  <li>Make first cut on elastomer selection</li>
  <li>
    Physical Property Requirements
    <ul style="font-style: normal; list-style-type: disc;">
      <li>Durometer</li>
      <li>Compression Set</li>
      <li>Tear Strength</li>
      <li>Resilience or Damping</li>
      <li>Spring Rate or Modulus</li>
      <li>Tensile Strength</li>
      <li>Elongation</li>
    </ul>
  </li>
  <li>
    Determine Probable Failure Mode
    <ul style="font-style: normal; list-style-type: disc;">
      <li>Fatigue Resistance</li>
      <li>Heat/ Age/ Fluid Resistance</li>
      <li>Tear/ Wear/ Abrasion Resistance</li>
      <li>Hysteresis/ Heat Buildup Resistance</li>
      <li>Impact Resistance</li>
    </ul>
  </li>
  <li>Make second cut on elastomer selection</li>
  <li>
    Special Properties or Needs
    <ul style="font-style: normal; list-style-type: disc;">
      <li>Electrical - Conductivity/ Resistivity</li>
      <li>Gas Permeability</li>
      <li>Surface Friction</li>
      <li>Flame - Resistance/ Optical Smoke Density</li>
      <li>Food Service - FDA</li>
      <li>Medical - Implant/ Skin Contact/ Outgassing</li>
      <li>Marking/ Non-marking</li>
      <li>Bonding to Substrates - Metal, Plastic, Fiber</li>
    </ul>
  </li>
  <li>
    Elastomer Cost
    <ul style="font-style: normal; list-style-type: disc;">
      <li>Amount of Elastomer per Part - higher %, higher cost sensitivity</li>
      <li>Part include Bonded Metal Insert - no inserts, higher cost sensitivity</li>
    </ul>
  </li>
  <li>Make Final Elastomer Selection</li>
</ol>
<br>

