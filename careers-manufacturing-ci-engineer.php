<h1>Manufacturing/CI Engineer</h1>

<h2>REPORTS TO: <span style="color: #343435;">Director of Operations</span></h2>
<br>

<h2>POSITION PURPOSE</h2>
To strategically identify, implement and communicate part-specific and systemic productivity improvements with a focus on visual inspection automation.
<br>
<br>

<h2>PRINCIPLE DUTIES</h2>
<ol>
  <li>
    <strong>Manufacturing</strong><br>
    Drive the successful implementation of projects to reduce manufacturing costs, improve product quality, increase throughput, and/or ensure employee safety through:
    <ul>
      <li>manufacturing process controls</li>
      <li>manufacturing resolution / process improvements</li>
      <li>new manufacturing processes and methodologies</li>
    </ul>
  </li>
  <li>
    <strong>Automation</strong><br>
    mplement, improve and maintain inspection automation systems.
    <ul>
      <li>Perform routine diagnostic checks on automated systems.</li>
      <li>Install, maintain, and replace automation systems at key points in the production process.</li>
      <li>Troubleshoot components including servos, sensors, and control components</li>
      <li>Log equipment usage, parts replacement, and scheduled maintenance to ensure maximum functionality.</li>
    </ul>
  </li>
  <li>Regularly report on project status and effectiveness.</li>
  <li>Recommend new capital equipment; oversee its installation and start-up; train operators; write operating manuals/procedures.</li>
  <li>Performs other duties as assigned.</li>
</ol>
<br>

<h2>QUALIFICATIONS</h2>
<ol>
  <li>Bachelor of Science degree in Mechanical Engineering, Industrial Engineering, or Associate's degree in automation technology, mechatronics or robotics. </li>
  <li>Demonstrated troubleshooting, analytical and project management skills is manufacturing environment. Must be "hands on"</li>
  <li>Proven ability to work independently and in direct contact with manufacturing shop floor personnel, suppliers and customers.</li>
  <li>Visual inspection experience in automation is a plus.</li>
  <li>Strong verbal and written communication skills across all levels of the organization.</li>
</ol>

