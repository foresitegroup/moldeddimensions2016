<h2>Test Methods for Determining Adhesion Values</h2>
Adhesion testing is done in several ways, depending upon the application and the product design. The methods recognized for this testing are treated in full detail in ASTM Test Method D429. These methods are:<br>
<br>

<strong>Method A.</strong> Rubber adhered between two parallel metal plates.<br>
<br>

<strong>Method B.</strong> Ninety degree stripping test, rubber adhered to one metal plate.<br>
<br>

The above methods are used primarily for laboratory development and testing production parts. These methods may be modified and applied as described under RMA Production Test Methods section as follows.<br>
<br>

<h1>RMA PRODUCTION TEST METHODS</h1>
<strong>Method A.</strong> Used where two metal surfaces, not necessarily parallel, can be separated until the specified adhesion value is obtained using the projected adhered area. The area to be considered should be the projected active adhered working area of the smallest metallic member, excluding fillets, overedge, and radii. Very irregular areas are to be given special consideration.<br>
<br>

<strong>Method B.</strong> Used where the rubber can be stripped from the entire width of the part to obtain a specified adhesion value or where the rubber can be cut in 25.4mm (1.0in) wide strips. Specimen rubber thickness shall not exceed 9.5mm (.375 in). In rubber sections over 9.5mm (.375in), values should be negotiated between customer and supplier.<br>
<br>

<strong>Acceptance Criteria</strong> Looseness contiguous to the adhered areas at corners, fillets, mold parting lines, and back-rinding will ordinarily be acceptable.<br>
<br>

The adhesion strength is usually considered to be satisfactory if the failure causes permanent distortion of a metallic member.<br>
<br>

If the deformation of the rubber section under test far exceeds the functional service requirements, this factor should be taken into consideration when establishing a reasonable adhesion value.<br>
<br>

It is recognized the conditions for adhesion will exist where a quantitative value cannot be obtained. In these instances, it is customary to pull the rubber from the metallic member and examine the nature of the failure. The acceptable degree of adhesion must be agreed upon between the customer and the rubber manufacturer. Customer's test methods and fixtures should be identical with those of the rubber manufacturer and correlation procedures established.<br>
<br>

<h1>METHODS OF DESIGNATING ADHESION VALUES</h1>
The design engineer when writing specifications should use a designation to obtain suitable adhesion for the purpose intended.<br>
<br>

Methods of testing, such as tension pull or shear pull (RMA Production method "A") or 90 degree stripping (RMA Production Method "B") and the minimum destruction values, as well as the design of special testing fixtures should be specified on the drawings. ASTM D2000-SAE J200 has two types of adhesion designations for adhesion of vulcanized rubber to metal:<br>
<ol type="1" style="font-style: italic;">
  <li>Adhesion by vulcanization, designated by K11 or K21.</li>
  <li>Adhesion by the use of cements or adhesives after vulcanization, designated by K31.</li>
</ol>

This section is concerned only with K11 and K21.<br>
