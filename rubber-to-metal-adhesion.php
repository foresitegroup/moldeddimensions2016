<h1>RUBBER TO METAL ADHESION</h1>
<h2>Introduction</h2>
The processes of adhering rubber to metal components are widespread techniques in the rubber industry. Generally the same considerations and procedures are applicable for rubber to rigid non-metallic components, but the adhesion values may be lower. Only the broad aspects of rubber-to-metal molding are covered here, and more precise information can be provided by the rubber manufacture involved.<br>
<br>

<h1>GENERAL INFORMATION</h1>
<h2>Application</h2>
Various adhesion levels can be obtained. For instance, to obtain adhesion on critical products, such as engine mounts, very close controls are required, both on metal and rubber preparation. Some products may require only enough adhesion for assembly.<br>
<br>

The adhesion level (tear/tensile strength) is directly affected by type of metal, surface preparation, non-metallic inserts, compound composition, curing conditions, and type of adhesive.<br /> <br /> Drawings should clearly state adhesion requirements and any other factors which can explain the degree of adhesion required and the method of testing. A clear understanding between customer and rubber manufacturer is essential.<br>
<br>

<h2>Methods of Obtaining Adhesion</h2>
The method most commonly used to obtain adhesion between rubber and metallic or non-metallic components is the use of adhesive cements. Prior to the use of these special adhesives, the surface of the insert must be clean and free of contamination.<br>
<br>

The inserts may be prepared by suitable methods such as degreasing, blasting, and/or a suitable chemical treatment. When any one of these preparatory processes is objectionable, it should be noted on the drawing. The rubber compound is then vulcanized to the prepared inserts to obtain the desired adhesion.<br>
<br>

<h2>Design Factors and Limitations</h2>
<ol type="a" class="one-half-left" style="font-style: italic;">
  <li>Avoid localized stress raising irregularities.</li>
  <li>Minimize edge effects. Break, coin or otherwise eliminate sharp edges of all metallic members covered by the rubber.</li>
  <li>Minimize surface roughness of metallic members in area adjacent to adhered rubber.</li>
  <li>Avoid welding a molded rubber component to a machine or structure to prevent unnecessary heat deterioration. When welding is mandatory, design metallic member as a heat sink and provide for assembly techniques which will keep the adhered rubber area of the metallic member below 150&deg;C (302&deg;F).</li>
</ol>

