<div class="one-third-left">
  <h2>Polyurethane-to-Metal Bonding</h2>
  The generous use of fillets or overhangs serves to distribute stress over a broader area, thus improving the bond and increasing the useful life of the part.
</div>

<div class="two-thirds-right"><img src="images/urethanedesign1b.gif" alt=""></div>

<div style="clear: both; height: 0;"></div>

<div class="one-third-left">
  <h2>Equalization of Stress</h2>
  Proper design tends to equalize stress distribution.<br>
  <br>

  <h2>Summary</h2>
  When designing polyurethanes for dynamic applications, avoid:<br>
  <ul>
    <li>Sharp corners at edges of bonded areas</li>
    <li>Sharp angles in the polyurethane which tend to focus stresses</li>
    <li>Sharp lips or edges which would cut or magnify stress concentration</li>
    <li>Complicated parts which, in addition to requiring expensive molds, have many points of stress concentration</li>
    <li>Designs which prevent natural shaping of the polyurethane when under a load</li>
  </ul>
</div>

