<h1>Quality Engineering Manager</h1>

<h2>REPORTS TO: <span style="color: #343435;">Vice President of Technical Services</span></h2>
<br>

<h2>POSITION PURPOSE</h2>
To pursue customer satisfaction and help meet company objectives of quality, cost and delivery through leadership in problem resolution and the understanding of and adherence to standards and best practices.<br>
<br>

<h2>PRINCIPLE DUTIES</h2>
<ol>
  <li>
    Manage company Quality Systems, including:
    <ol type="a">
      <li>Customer Complaints and the RGA process</li>
      <li>ISO 9001:2015 QMS; Internal and Registrar audits</li>
      <li>Customer audits and assessments</li>
      <li>Inspection, measuring and test equipment selection, maintenance, and calibration</li>
      <li>New Product PPAP process / First Article Inspection</li>
      <li>Entry of Bills of Material, Routings/Process Sheets into the business system</li>
      <li>Defect limit samples and in-process inspection/test methods</li>
      <li>Raw Material Receiving Inspection</li>
      <li>Supplier Quality</li>
    </ol>
  </li>
  <li>Lead corporate Continuous Improvement efforts.</li>
  <li>
    Support other departments:
    <ol type="a">
      <li>Engineering, with product quality planning, including safe launches</li>
      <li>Purchasing, with supplier quality issues and supplier development</li>
    </ol>
  </li>
  <li>Coach and develop Quality Technicians. Define roles and responsibilities. Determine human and capital resource needs. Cross-train to minimize organizational risk.</li>
  <li>Foster a customer-focused culture. Help train personnel in customer quality expectations, inspection methods, and data collection and analysis.</li>
</ol>
<br>

<h2>QUALIFICATIONS</h2>
<ol>
  <li>Bachelor's degree in Engineering</li>
  <li>Experience investigating and resolving customer complaints</li>
  <li>Working knowledge of PPAP requirements, Lean Manufacturing concepts, and blueprint reading</li>
  <li>Desire and ability to contribute as a manager and a doer</li>
  <li>Fluency in ISO 9001:2015 and lead auditor experience</li>
  <li>Successful track record of working cross-functionally</li>
  <li>Strong verbal and written communication skills</li>
  <li>Supervisory experience a plus</li>
</ol>

