<h1>Talent Acquisition & Retention Specialist</h1>

<strong>SUMMARY<br>
The Talent Acquisition & Retention Specialist is responsible for developing and implementing recruiting strategies to secure talent for all open positions as well as strategies to maintain employee engagement.</strong><br>
<br>

<strong>DUTIES AND RESPONSIBILITIES</strong><br>
<ul>
  <li>Coordinates with hiring managers to identify staffing needs</li>
  <li>Develops and updates job descriptions as necessary</li>
  <li>Advertises job openings on career pages, job boards and social networks</li>
  <li>Develops a talent pipeline of qualified candidates</li>
  <li>Screens candidates, provide on-site tours and interviews for production roles</li>
  <li>Coordinates interviews with hiring managers for professional roles.</li>
  <li>Manages the onboarding process including orientation, buddy program and 30/90 day follow-ups</li>
  <li>Research and implement new sources for candidate recruiting.</li>
  <li>Utilizes multiple resources in recruitment process, including job fairs, internet, and social and professional networks.</li>
  <li>Develops a strong employer brand</li>
</ul>
<br>

<strong>QUALIFICATIONS</strong><br>
<ul>
  <li>Minimum of 3 years of HR and recruitment experience, manufacturing environment a plus.</li>
  <li>Bachelors or Associates degree in human resources or related field preferred.</li>
  <li>Excellent communication and interpersonal skills.</li>
  <li>Creative, organized and detailed oriented.</li>
  <li>Proficient in Microsoft Office (Excel, Word, PowerPoint, Outlook)</li>
  <li>Ability to multi-task, prioritize and work efficiently.</li>
  <li>Ability to work independently.</li>
</ul>
