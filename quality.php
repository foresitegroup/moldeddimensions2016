<h2>Project Review</h2>
From the inception of a project, Quality plays a major role. We work hard to learn, and respond to, a customer's unique quality concerns. Our Sales Engineers utilize the design review and RFQ process to learn and understand the part, its application and working environment. The information gained helps MDI design tooling and a manufacturing process to meet your quality expectations.<br>
<br>

<h2>Design Review</h2>
At project kickoff, we utilize our cross-functional <a href="project-management.php">project management</a> team to initiate the project, design robust tooling, and structure the best manufacturing process. In addition to this, we incorporate your requirements into the incoming material requirements, process requirements, finishing standards, and inspection requirements for the product.<br>
<br>

<h2>Initial Sample Process</h2>
<img class="one-half-right" src="images/quality2.jpg" alt="Sample inspection of thermoset Nitrile tubes and hoses.">
Engineering, Quality, Manufacturing and Customer Service participate in establishing and confirming the specific steps that ensure delivering the customer's product problem free and on-time, the first and every time. Our engineering Project Manager leads the team regarding all aspects of the project including procurement, internal and external communication, sample production, and sample inspection. By design, involved operators are trained to inspect and statistically sample per the customer's specific requirements. Our quality documentation includes ISIRs, SPCs, PPAPs, FMEAs and APQP. Quality is again reinforced with a post part approval meeting to ensure a robust, production-ready process.<br>
<br>

<h2>On-Going Quality</h2>
It is critical to manufacture quality parts at each and every step along the way. To accomplish this, all MDI employees are inspectors. Another key part of our on-going quality plan is our weekly, cross-departmental continuous improvement meetings. In these meetings, our manufacturing, engineering, and quality personnel work on improvements for upcoming work. Both of these strategies have decreased our cost of poor quality, customer returns and in-process scrap to industry record lows.<br>
