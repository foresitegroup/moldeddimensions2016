95% of a product's cost and repeatability is decided at product launch, and as such, mold design and construction are a critical part of Molded Dimensions' project management process.  Our experienced mold manufacturing group works hand-in-hand with our project management team to provide design for manufacturability assistance to ensure a robust part design, an efficient molding process, and a durable mold for the life of the project.<br>
