Note that these two tests give different values. The differences in performance of vulcanizates of urethane can be explained however. The NBS test simulates a very harsh service. In this case, the hardest vulcanizates hold up best. The Taber test is much less severe. In this case, the hardest vulcanizates hold up best. The Taber test is much less severe. Softer compounds perform better than the harder ones because they are more resilient and "give" under load.<br>
<br>

The resistance to sliding abrasion increases with increasing hardness for the NBS test. The unusually high NBS Index for the 75A durometer vulcanizate is probably due to lubrication of the abrasive wheel by the plasticizer used in the compound.<br>
<br>

As the hardness of urethane increases, NBS values increases for non-plasticized compounds.<br>
<br>

In spite of the difficulties in obtaining meaningful laboratory abrasion test values, urethane is consider to have excellent sliding abrasion resistance and has performed well in many applications where wear is a problem. Urethane has outworn conventional rubber and plastics often by a factor of as much as 8 to 1.

