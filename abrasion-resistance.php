<h1>Abrasion Resistance</h1>
There are two types of abrasion - sliding and impingement. Sliding is the passing of an adjacent surface across the rubber surface. Impingement is wearing of the rubber exemplified by sand particles hitting the surface. Most wear in actual service occurs as a combination of both sliding and impingement.<br>
<br>

When sliding, localized friction forces can impose high energy levels on the rubber. Abrasion and wear takes place when the rubber cannot withstand these forces.<br>
<br>

Impingement by particles occurs in applications such as chutes, rebound plates and sandblast hose. Elastomers can yield easily and distribute stresses imposed by particle impingement. A sandblast test shows that with a 90 degree impingement angle, soft resilient rubber is more abrasion resistant that steel or cast iron. However, not just any elastomer can be used. Under this same condition, a tough tire tread will wear out more rapidly that a soft elastomer. The angle of particle impingement has a significant effect on which material should be used. The angle of particle impingement has a significant effect on which material should be used. As the angle decreased below 90 degree, the superiority of elastomers over metals declines and disappears.<br>
<br>

Laboratory abrasion tests are difficult to correlate with end-use applications. Measurement of properties can be helpful in selection of materials, but do not compare to rates in actual service which can be thousands of times greater with regard to velocities and temperatures.<br>
<br>

There are at least 25 laboratory abrasion test devises, an indication that this type of test is difficult to correlate with service performance. The most widely recognized test device in the rubber industry is the National Bureau of Standards Abrader, a slinging type abrader. The NBS Abrader uses a constant velocity, under a fixed load using a specified abrasive grit.<br>
<br>

It does not tell how a compound performs under widely varying conditions, nor does it tell anything about cut resistance, chunking, or flat spotting.<br>
<br>

