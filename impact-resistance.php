<h1>Impact Resistance</h1>
The impact or shock resistance of an elastomer is determined by striking a sample with a swinging pendulum (hammer). The sample is placed at the lowest point of the arc traveled by the pendulum head. Measuring the difference in the distance of the upswing of the pendulum after the impact, compared to the same upswing with nothing in its path, determines the energy in breaking the sample which is the measure of impact strength.<br>
<br>

In the Izod impact method, (ASTM D-256) the test piece is gripped upright and struck with the pendulum 7/8" above the edge of the gripping point. The sample has a standardized notch at the edge of the vise on the side toward the hammer. The Izod Impact Tester and Izod Bar are shown in Figures 1, 2, and 3.<br>
