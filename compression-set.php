<h1>Compression Set</h1>
Compression set, creep and stress relaxation are related to the fluid characteristics of elastomers. Set is defined as the deformation remaining after removal of the deforming stress. Creep involves the increase in deformation with time under constant stress. Stress relaxation is the decrease of stress with time at a constant deformation.<br>
<br>

<h2>Compression Set</h2>
Compression set tests, described in ASTM D-395, are of two main types: Method A, compression set under constant load; and Method B, compression set at constant deflection of 25%.<br>
<br>

In Method A, specimens of standard dimension are compressed between parallel steel plates under a stress of 400 psi. The test assembly is then conditioned for a selected time at the selected test temperature (such as 22 hrs at 158F (70C)) after which the specimens are removed and allowed to recover at room temperature for 30 minutes. Compression set is the difference between the original thickness of the specimen and the thickness after test, expressed as a percentage of the original thickness.<br>
<br>

In determining compression set by Method B, the specimen is compressed to 75% of its original thickness. The test assembly is then conditioned for the specified time at the selected test temperature. Compression set determined by Method B is the difference between the original thickness of the specimen and the thickness after testing, as a percentage of the deflection employed.<br>
<br>

Although the conditioning time and temperature are specified in the ASTM standard, other times and temperatures are frequently used.<br>
<br>

Compression set is applicable particularly to the compounds used in machinery, motor mountings and vibration damping. Compression set tests are intended to measure the ability of elastomeric vulcanizates to retain elastic properties during the prolonged action of compression stresses. The actual stressing in service may involve (1) the maintenance of a definite deflection, (2) the constant application of a known load, or (3) the rapidly repeated deformation and recovery from compression forces.<br>
<br>

There are applications where the temperatures and deformation conditions used in the permanent set test are approximated in actual service. These instances, where apparent similarity exists, have led to a widespread tendency to overemphasize permanent set values. Since the shortest testing time will never approach the much greater span of desire service life, the test values will only suggest, not predict, what may be expected in service. For example, it is often thought that low compression set is always accompanied by high resilience and low creep. While trends of this type may be evident when considering extreme values for compression set, there are so many exceptions that acceptance of the general statement does more harm than good.<br>
