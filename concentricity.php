<h1>CONCENTRICITY</h1>
<img src="images/standfigure2.gif" alt="" class="right">
Concentricity is the relationship of two or more circles or circular surfaces having a common center. It is designated at TIR (total indicator reading) and is the total movement of the hand of an indicator set to record the amount that a surface varies from being concentric.<br>
<br>

All diameters formed in the same mold plate will be concentric within 0.25mm TIR (.010 in TIR).<br>
<br>

Example:<br>
In Fig 2 diameter "A" will be concentric with diameter "B" within 0.25mm TIR (.010 in TIR).<br>
<br>

Other diameters will be concentric within 0.75mm TIR (.030 in TIR).<br>
<br>

Example:<br>
In Fig 2 diameter "A" or "B" will be concentric with diameter "C" within 0.75mm TIR (.030 in TIR).
