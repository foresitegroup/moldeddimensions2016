<h1>SURFACE FINISH AND APPEARANCE</h1>
<h2>Introduction</h2>
The purpose of this section is to list and discuss some of the factors that have an effect on the finish and appearance of molded products and to present standards covering four classes of finish.<br>
<br>

<h1>FACTORS AFFECTING FINISH AND APPEARANCE</h1>
<h2>Machined Finish of Mold</h2>
The machined finish of the mold has considerable effect on the surface finish or appearance of a rubber product.<br>
<br>

The best finish can be obtained from a highly polished steel mold, free from all tool marks or other imperfections. Naturally, this type of mold is quite expensive to construct and maintain and is not generally required unless surface finish is of paramount importance from either an appearance or functional standpoint. In addition, it may be desirable in some cases to chrome plate the mold in order to maintain the required surface finish under production conditions.<br>
<br>

The commercial type mold is a machined steel mold made to conform to good machine shop practice. Machine tool marks will not ordinarily be polished out of this type of mold. It should be noted that regardless of how highly the mold itself is polished, the appearance of the rubber surface will depend to a large extent upon the factors outlined in the following paragraphs.<br>
<br>

<h2>Type of Rubber Material Used</h2>
The type of rubber material used can greatly affect the appearance of the rubber product. Some compounds lend themselves to a bright glossy surface while others may be dull as molded or become dulled very easily during handling or storage. Also, there are some rubber compounds to which antiozonants are added to impede attack from ozone. As these compounds age, the antiozonants "bleed out", giving the product a colored or waxy surface. This is a common practice and the product should not be considered imperfect or defective in any way. This or other specification requirements may make it impossible to produce a product with a glossy surface.<br>
<br>

<h2>Mold Release Used</h2>
There are certain rubber compounds that can be removed from the mold with the use of little or no mold release lubricant, while others require the use of considerable quantity of mold release lubricant. The latter may have the appearance of being oily.<br>
<br>

If the surface of the rubber product is to be bonded to other materials in its application or is to be painted, the designer should designate this on the drawing so that the manufacturer may use a mold release lubricant that will not impair adhesion quality.<br>
<br>

<h2>Flash Removal Method</h2>
Some of the many methods used to remove flash from rubber parts may affect the appearance of the finished product. As an example, hand trimming will ordinarily have no effect, while tumbling may result in a dull surface.<br>
<br>

