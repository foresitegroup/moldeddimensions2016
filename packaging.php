<h1>PACKAGING</h1>
When a rubber part is packaged, it is the sole purpose of transportation from the supplier to the user. Packaging usually causes some distortion of the rubber part which, if used in a reasonable length of time, does not permanently affect the part. However, when rubber parts are held in a distorted position for a prolonged period of time, permanent set may cause permanent distortion and result in unusable parts. Any product on which distortion may make the part unusable should be specified and packaged by such methods as will prevent distortion. However, such methods are sometimes costly and should not be specified unless absolutely necessary. When distortion is a problem, the product should be removed from the container when received and stored on shelves or in a manner to preserve usability. Packaging is a complex area and should be given serious consideration. Table 11 is to be considered only as a guide. Special packaging problems should be considered between purchaser and supplier.<br>
