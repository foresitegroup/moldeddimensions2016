<h1>Customer Service / Order Processing</h1>

Under the direction of the VP Sales, the CS/OP associate provides the best possible support of the customer and the manufacturing departments at MDI to assure open communication and accurate order data within the MDI system, in accordance with company policies and procedures.  Responding to customer requests and inquiries and assisting in resolving issues involving customer orders.<br>
<br>

<strong>Essential Duties and Responsibilities</strong><br>
This list of duties and responsibilities is not all-inclusive and may be expanded to include other duties and responsibilities, as management may deem necessary from time to time.
<ol>
  <li>Contract Review: Audits purchase order for price, part number, revision level, shipping locations, and notes.</li>
  <li>Processes and enters purchase order information into computer system.  Includes forwarding orders to shop and sending confirming orders to customer.  Files completed documents.</li>
  <li>Takes calls, faxes, and emails from customers or manufacture representative regarding new order delivery information, change orders, pricing information, and status of open orders.</li>
  <li>Communicates delivery changes as reported by production to customers.</li>
  <li>Interfaces with production control, production manager, quality assurance, and engineering personnel in an effort to solve customer concerns.</li>
  <li>Achieves and maintains rapport with customers and works to give them the best possible service.</li>
  <li>Responds to managers' request for assistance on appropriate additional assignments.</li>
  <li>Ability to work in customer Wed based Portals to retrieve orders and complete ASN's as needed.</li>
</ol>
<br>

<strong>Qualifications</strong><br>
To perform this job successfully, an individual must be able to perform each essential duty satisfactorily. Knowledge of general order processing and procedures in a manufacturing environment are required.  Accurate and attention to detail required.<br>
<br>

<strong>Education and/or Experience</strong>
<ul>
  <li>4-year high school diploma required.</li>
  <li>2 years experience in office environment, manufacturing preferred.</li>
  <li>Data entry and basic computer skills in MS office.</li>
  <li>Experience in using email as method of corresponding with customer and co-workers.</li>
  <li>Understanding of job shop production processes, exposure to basic ERP system.</li>
  <li>Exposure to blueprints helpful.</li>
</ul>
<br>

<strong>Language Skills</strong><br>
Ability to read and interpret documents such as customer Purchase Orders, Release Sheets, Packing Lists, and web based Portal information. Ability to prepare routine reports and correspondence. Ability to communicate effectively with customers and employees at all levels of the organization.<br>
<br>

<strong>Mathematical Skills</strong><br>
Ability to add, subtract, multiply, and divide in all units of measure, using whole numbers, common fractions, and decimals. Ability to compute rate, ratio, and percent.<br>
<br>

<strong>Reasoning Ability</strong><br>
Ability to apply common sense understanding to carry out instructions furnished in written, oral, or diagram form. Ability to deal with problems involving several concrete variables in standardized situations.<br>
<br>

