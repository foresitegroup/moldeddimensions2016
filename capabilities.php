<h2>Raw Material Type</h2>
<img class="one-half-right" src="images/capabilities2.jpg" alt="Cast urethane poured into an open cast urethane mold.">
MD customers benefit from our ability to process both molded rubber and cast polyurethane. These thermoset elastomers utilize a process known as vulcanization. Vulcanization, or curing, causes chemical cross-linking of shorter polymer chains into longer polymer chains. The long chains created during vulcanization give the material its' elastic property. Elasticity comes from the ability of the long chains to deform under stress and return to their original shape. The vulcanization process is irreversible, much like cooking an egg, since the material cannot be "melted" and cured again. Available rubber polymers range from natural rubber to synthetic polymers like polychloroprene, Nitrile, EPDM, Silicone, fluoroelastomers and Perfluoroelastomers.<br>
