<h1>Shear</h1>
Mounting and suspension assemblies generally require the loading of elastomers in shear. Elastomers deflect more under a given load in shear than in compression. Since shear is essentially a combination of tensile and compression forces acting at right angles to each other, the stress strain curve for an elastomer in shear is similar to the tensile and compressive stress-strain curves. Shear is the ratio of linear deformation (d) to elastomer thickness (t) as illustrated in Figure 1.<br>
<br>

