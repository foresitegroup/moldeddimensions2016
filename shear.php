<br>

Figure 2 shows typical shear stress-strain curves for urethane ranging in hardness from 55A to 75D durometer.<br>
<br>

<img class="centered-image" src="images/shear2.gif" alt=""><br>
<br>

Because of its high load bearing capacity in tension and compression, urethane has a high load bearing capacity in shear. The factor which limits the use of urethanes in shear loading is the strength of the metal adhesive bond rather than the shear strength of the polymer.<br>
<br>

Improvements in bonding urethane to metal will permit greater stress than those shown in Figure 2. Presently, 300 pli adhesion can be achieved compared to those values shown which are based on 100 pli.<br>
<br>

Past practice has limited shear strain (t) to 0.5; that is, the thickness of the rubber is twice the horizontal deflection. No specific reasons can be cited for this limitation. Some static applications of shear loading have been deformed to strains of 1.0 or more. However, under high strain, bond failures can occur imposing high stresses on the part. Useful hardnesses of urethanes are limited from 65A to 90A durometer. Below 65A conventional rubber can be used, and above 90A stresses are very unpredictable.<br>
<br>

It is common practice to enclose a shear mounting and move the loading surfaces closer together to provide a compressive load on the elastomers. Compression of 5% of the free thickness is commonly used. The effect of shear loading for a double shear pad is shown in Figure 3<br>
<br>

<img class="centered-image" src="images/shear3.gif" alt=""><br>
<br>

With load, the rubber tends to leave the supporting walls at the top. As the angle decreases, diagonal A decreases in length thus creating compression at X. But diagonal B increases in length causing tension at Y. Therefore, by moving the loading surface closer together, the tensile stresses are reduced.<br>
<br>

To achieve stability, the ration of width and length to thickness should be at least four. Lower ratios probably can be used with urethane and still be stable. If a shear pad were so designed that the height of the rubber equaled its thickness, the rubber would tend to bend as a cantilever beam rather than as a shear mounting.<br>
<br>

If larger deflections are required than can be accommodated by one thickness, it may be necessary to make several sandwiches in shear as shown on Figure 4.<br>
<br>

<img class="centered-image" src="images/shear4.gif" alt=""><br>
<br>

However, the total width of the part between supports cannot be made too wide. Even though the elastomer is broken up into several sandwiches between supports, instability results in deflections greater than calculated from plain shear.<br>
<br>

Shear bonds are affected by the thickness of the sandwich. The greater the thickness, the higher the tensile component in shear which results in less bond strength as shown in Figure 5.<br>
