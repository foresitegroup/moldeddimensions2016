<h1>Senor Buyer</h1>

To manage the process of sourcing and purchasing of essential materials or services and monitoring inventory control.<br>
<br>

<strong>Principle Duties</strong><br>
<ol>
  <li>Oversee supply chain and maintains inventory of all materials.</li>
  <li>Develops and maintains relationships with wholesalers and suppliers</li>
  <li>Negotiate pricing, lead times, and terms/conditions.</li>
  <li>Researches, analyzes, and forecasts current and future buying trends</li>
  <li>Assesses financial risks of purchases.</li>
  <li>Establish alternate suppliers, both domestically and internationally.</li>
  <li>Attends trade shows, product exhibitions, and conferences to maintain understanding of new products and trends.</li>
  <li>May oversee the scheduling, assignments, and daily workflow of indirect subordinate staff</li>
  <li>Performs other duties as assigned.</li>
</ol>
<br>

<strong>Qualifications</strong><br>
<ol>
  <li>Bachelor of Science degree in Operations, Supply Chain or Procurement</li>
  <li>7 to 10 years' experience in sourcing domestically and internationally</li>
  <li>Proven ability to work independently and in direct contact with manufacturing shop floor personnel, suppliers, and customers.</li>
  <li>Excellent interpersonal and negotiation skills</li>
  <li>Strong verbal and written communication skills across all levels of the organization.</li>
  <li>Excellent organizational skills and attention to detail.</li>
  <li>Strong analytical and problem-solving skills</li>
  <li>Ability to function will in a high-paced and, at times, stressful environment</li>
  <li>Proficient with Microsoft Office Suite or related software.</li>
</ol>
