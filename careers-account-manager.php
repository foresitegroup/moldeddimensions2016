<h1>Account Manager</h1>

Under the direction of the VP of Sales & Marketing, the Account Manager provides the best possible support to the customer and the manufacturing departments at Molded Dimensions to assure open communication and accurate order data within the Molded Dimensions system, in accordance with company policies and procedures.  Responding to customer requests and inquiries and assisting in resolving issues involving customer orders. This person will be responsible for all customer relationships after the part is moved into production.<br>
<br>

<strong>Principle Duties</strong><br>
<ol>
  <li>Contract Review: Audits purchase order for price, part number, revision level, shipping locations, and notes.</li>
  <li>Processes and enters purchase order information into business system.  Includes forwarding orders to production and sending confirmation orders to customer.  Files completed documents.</li>
  <li>Takes calls, faxes, and emails from customers or manufacture representative regarding new order delivery information, change orders, pricing information, and status of open orders.</li>
  <li>Communicates shipping notices and delivery changes as reported by production to customers.</li>
  <li>Interfaces with production control, production manager, quality assurance, and engineering personnel in an effort to solve customer concerns.</li>
  <li>Develops and maintains rapport with customers and works to give them the best possible service.</li>
  <li>Provides pricing adjustments and proposals for production items as needed.</li>
  <li>Responds to managers' request for assistance on appropriate additional assignments.</li>
  <li>Work in customer Web based Portals to retrieve orders and complete ASN's as needed.</li>
  <li>Travel to customers occasionally.</li>
</ol>
<br>

<strong>Qualifications</strong><br>
<ol>
  <li>High school diploma required.</li>
  <li>2-4 years experience in office environment, manufacturing preferred.</li>
  <li>Data entry and basic computer skills in MS office.</li>
  <li>Experience in using email as method of corresponding with customer and co-workers.</li>
  <li>Understanding of job shop production processes, exposure to basic ERP system.</li>
  <li>Exposure to blueprints helpful.</li>
  <li>Good communication and organizational skills.</li>
</ol>
