Compression strain can be considered to be extensions of tensile strain which are continuous through the origin. However, the compressive samples must be free to move, ie, the faced must be lubricated. Generally, rubber in compression is bonded to the surface or surface friction restricts movement. Compression curves are usually smooth and do not exhibit the "S" shape usually found in tensile tests. Compressive strain (Fig 2) is limited to less than 100% and, therefore, the curve becomes asymptotic to the 100% line.<br>
<br>
<br>

<img class="centered-image" src="images/compressionfigure2.gif" alt=""><br>
<br>

While the ability to deform under compressive stress and then recover is a characteristic property of elastomers, other factors, notably the shape of the part, affect the way an elastomer deforms in compression. To illustrate, consider two blocks cut from the same piece of rubber. One is a cylinder with the proportions of an ice-hockey puck, the other is a block of the same height and cross-sectional area, but rectangular in shape. If equal weights are placed on the blocks, subjecting them to the same compressive stress, the rectangular block will deflect more than the cylinder (Fig 3). Since the blocks will not change in volume, the reduction in height is caused by the freedom of the sides to bulge. The rectangular block deflects more than the cylindrical one because the sides of the rectangular block provide a greater area free to bulge.<br>
<br>
<br>

