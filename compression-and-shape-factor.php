<h1>Compression and Shape Factor</h1>
When a load is applied to an elastomer, it "flows" in accordance with the force exerted on it and within the limits provided by the mass of the material itself or by the dimensions of its container. In rubber technology this is called compression. Although this term is correct in the framework of normal rubber usage, it may be misleading to the engineer. It does not mean that the elastomer will undergo a change in volume under pressure. Rather, it means the elastomer will deflect, or undergo a change in shape. This distinction is important. An elastomer is an incompressible fluid, capable of changing its shape to the limit of its strength under load. It will react to a load placed upon it by tending to exert force uniformly in all directions. This is illustrated in Figure 1. Even though the elastomer is changed in shape under load, it is compelled by the characteristic of elasticity to return to its original shape once the load is removed.<br>
