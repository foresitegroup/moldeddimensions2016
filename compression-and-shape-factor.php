<br>

The designer of elastomeric parts allows for this behavior by using a concept called shape factor. Shape factor describes the role of the shape in determining how a part with parallel load faces will behave under compressive forces.<br>
<br>

The concept of shape factor is useful for the design engineer. If the elastomeric part does not deflect enough to do its job, the designer can reduce the shape factor by increasing the thickness of the pad. In reality, he does no more than increase the area free to expand under load. If the pad deflects too much, he may decrease the area free to expand he may increase the hardness of the elastomer.

