<h1>STATIC AND DYNAMIC LOAD DEFLECTION</h1>
<h2>Introduction</h2>
Primarily, rubber is used in place of metallic, ceramic, and other rigid materials because it will provide a greater deflection for a given force than these other materials. Most uses of rubber are based upon this characteristic.<br>
<br>

In many uses of rubber, stiffness variation is not critical to the rubber product function and in such cases the Shore A durometer hardness specification is sufficient.<br>
<br>

Rubber is used as an engineering material in resilient mountings, vibration isolators, dampers, impact pads and many similar applications. Where static or dynamic stiffness characteristics become critical to the function of the product, appropriate test specifications must be established.<br>
<br>

<h1>METHODS AND CONSIDERATIONS</h1>
<h2>Static Methods</h2>
When a static load-deflection specification is established for a product, in addition to a hardness requirement, the load-deflection specification shall supercede the hardness, should be stated on the product drawing, and agreed upon between the customer and the rubber manufacturer. A static test is only "static" in that the load application comes to rest before the measurement is taken or the rate of deflection does not normally exceed 0.8mm/s (2 in/min). Such a test usually places the rubber in shear or compression. There are several ways of specifying static load-deflection characteristics:<br>
<ol type="a">
  <li>Specify spring rate in load per unit deflection, eg, Nm (lb/in) or torque per radian, eg, Nm/rad (lb-in/rad).</li>
  <li>Specify a load to deflect the product within a specified deflection range.</li>
  <li>Specify a deflection resulting in a load within a specified load range.</li>
</ol>
<br>

<h2>Dynamic Methods</h2>
Applications where rubber is used as vibration isolators are dependent upon the behavior of the rubber under dynamic operating conditions.<br>
<br>

Rubber is stiffer dynamically than in a static mode; and, since the static to dynamic stiffness ratio varies with individual compounds, it may be advisable to specify the dynamic characteristics of the rubber for such applications.<br>
<br>

When dynamic stiffness or spring rate is specified, and is critical to the rubber product performance, the complete conditions and methods of measurement must be established between customer and rubber manufacturer.<br>
<br>

There are several methods of dynamic testing:<br>
<ol type="a">
  <li>Steady state Resonance</li>
  <li>Free Decay Resonance</li>
  <li>Steady State Non-Resonant</li>
  <li>Rebound Evaluation</li>
</ol>
<br>

<h1>FACTORS AFFECTING STATIC AND DYNAMIC LOAD DEFLECTION CHARACTERISTICS</h1>
<h2>Age</h2>
The aging of rubber compounds over a period of time is a complex process. The normal net effect of aging is an increase in modulus or stiffness. The magnitude of this change is dependent upon the specific material involved and the environmental conditions.<br>
<br>

Short term age, in the sense of the minimum number of hours which should elapse between molding and evaluation, is also a significant factor. Depending upon the nature of the product, the minimum period will vary from 24 hours to 168 hours.<br>
<br>

<h2>Dynamic History</h2>
The load-deflection characteristics of a rubber product are affected by the work history of that specific product. The initial loading cycle on a new part, or a part that has been in a static state for a period of time, indicates a stiffer load-deflection characteristic than do subsequent cycles. In static testing this effect becomes stabilized and the load-deflection characteristics normally become repeatable after two to four conditioning cycles.<br>
<br>

In dynamic testing, the conditioning period is normally selected as the time required to obtain reproducible results.<br>
<br>

<h2>Temperature</h2>
Temperature has an effect on spring rate-the higher the temperature the lower the spring rate, and the lower the temperature the higher the spring rate of a rubber product not under continuous tension.<br>
<br>

<h2>Test Conditions</h2>
The following details must be defined by the product drawing, or referenced specification, to insure relevant and consistent product performance evaluation:<br>
<ol type="a">
  <li>
    Mode of Test
    <ol type="1">
      <li>Tension, Shear or Compression. A schematic diagram depicting product orientation is highly desirable. The spring rate in the compression mode is always higher than the spring rate in the shear mode.</li>
      <li>Static or Dynamic</li>
      <li>The dynamic spring rate is always higher than the static spring rate.</li>
    </ol>
  </li>
  <li>
    Test Level and Control Mode
    <ol type="1">
      <li>Static testing load level or level of deformation, together with the appropriate limits on deflection or limits of loading in response to deformation, shall be stated.</li>
      <li>Dynamic load levels shall be identified by a plus (+) value for downward forces and a negative (-) value for upward forces. Dynamic tests utilizing deformation control shall be specified by double amplitude (total amplitude) values.</li>
    </ol>
  </li>
  <li>The amount and direction of preload, if required.</li>
  <li>The linear or angular rate of loading or cyclic frequency.</li>
  <li>The nature and number, or duration, of conditioning cycles required prior to the test cycle or test period.</li>
  <li>The ambient test temperature and the period of time the product is held at test temperature prior to evaluation.</li>
  <li>When the requirements are stated as "Spring Rate" the location on the load-defection chart at which the tangent is drawn, or the load levels between which an average is taken, must be indicated.</li>
</ol>
<br>

