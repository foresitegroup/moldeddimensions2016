Mechanical limitation of durometers and the way people use them cause hardness measurements to be inconsistent. Also, indenter penetration is reduced immediately since elastomer deformation occurs and all elastomers inherently try to yield. It is not uncommon to find a difference of 5 points in individual hardness readings of an elastomer specimen. Tabletop durometers can measure hardness more accurately, but they are not as convenient and are not used routinely.<br>
<br>

Frequently, hardness is assumed to correlate with stiffness (modulus), but this is not always true. Variations of a few points in hardness can show a marked difference in compression-deflection.<br>
<br>

A statistical determination has been made of the relationship between hardness and 100% modulus of Pentathane measured with durometers on the A and D scales. As expected as shown in Table II, the A scale is more reliable for predicting the modulus of the softer stock; the D scale should be used with the harder stocks.<br>
