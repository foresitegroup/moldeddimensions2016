<h1>Durometer / Hardness</h1>
Hardness, as applied to elastomers, is defined as the relative resistance of a surface to indention by an indenter of specified dimension under a specified load. The most commonly used measuring instrument is a durometer. Numerical hardness values are derived from the depth of penetration. The harder the sample, the further it will push back the indenter point and the higher the readings. On the durometer A scale, 0 is very soft, and 100 is very hard. Values are usually read immediately after firm contact has been established. The hardness range of elastomers is so broad that a single durometer cannot indicate practical measureable differences of hardness. For this reason durometers are available in more than one scale model, (eg., A and D scale durometers). The A scale durometer is widely used throughout the rubber industry. The durometer D model, which has a stiffer spring and a more pointed indenter, is used to measure the hardness of hard rubbers.<br>
<br>

