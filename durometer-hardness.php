The reliability of predicting modulus from either A or D scale is approximately +/-33%, for 95% confidence limits. In the low modulus range of less than 500 psi, predictability is 25%.<br>
<br>

The A scale should be used with moduli of 500 psi and under. The D scale is more useful for predicting moduli of more than 1000 psi. Either scale may be used for the intermediate areas as in Table II.<br>
<br>

One hundred percent modulus can be predicted to within +/- 115 at a level of 400 psi, ranging up to +/- 315 at 1150 psi using the A scale; using the D scale predictability ranges from +/-100 at 400 psi to +/- 1200 at 5000 psi.<br>
<br>

