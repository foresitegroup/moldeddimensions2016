All of the additives at a 10 part level (7.4% by weight of total compound) will reduce modulus, tensile and elongation. Additives which reduce the friction coefficient also improve abrasion resistance. The improvement in abrasion obtained with Teflon&reg; Fluorocarbon fiber addition is significant and was also observed during long-term friction tests.<br>
<br>

The 10 parts of additive is not necessarily the optimum. However, 10 parts offers significant frictional improvement over 5 parts and is not significantly inferior to 15 parts. The optimum level of additive, considering a balance of physical and frictional properties, probably falls between 5 and 10 parts.<br>
<br>

<h2>Effect of Additives on Bearing Performance</h2>
Both graphite and Teflon&reg; improve the performance of urethane in bearing applications. Even graphite, which results in an increased friction coefficient, will provide better bearing service. This unexpected improvement is probably due to the eventual formation of a graphite film at the load interface, and to the better thermal conductivity of the graphite-filled compound. Pressure-Velocity (PV) limit data for bearings based on urethane, Nylon 66 and Delrin acetal resins are shown in Figure 3.<br>
