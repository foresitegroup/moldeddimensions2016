<h2>Effect of Additives on Friction Properties</h2>
Additives may be used to alter the frictional proportion of urethane polymers. With urethane, powdered Teflon&reg; TFE fluorocarbon resin and Teflon&reg; TFE fluorocarbon fiber flock significantly reduce the coefficient of friction of this elastomer. Incorporation of graphite increases the coefficient of friction. The frictional change due to these additives is shown in Figure 2. Urethane without additives is used as a control.<br>
