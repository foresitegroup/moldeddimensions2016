<h1>Tear Resistance</h1>
Tear resistance is a complex result of other basic properties, such as modulus and tensile strength. Many laboratory methods have been devised to measure this property. The tests now being used to measure the tear resistance of elastomers are useful for laboratory comparisons, but correlation between test results and service performance is often quite difficult. The various tear tests produce different results when used with urethane rubbers. The following tear tests for urethane have been compared:<br>
<br>

ASTM D-470<br>
Instrom Split Tear<br>
ASTM D-751-52T (Modified Trapezoid)<br>
ASTM D624 Die C (Graves)<br>
ASTM D-624 Die B (Winkelmann)<br>
<br>

ASTM D-470 and the Instron split tear tests are least dependent on tensile strength and give the most realistic evaluation of the tear strength of urethane. Specimens used in each test are shown in Figure 1.<br>
