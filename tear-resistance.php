Results are computed in terms of force per inch thickness required to tear the specimen. For example, if a load of 40 pounds is necessary to tear a specimen 0.07 inches in thickness, the tear resistance would be 40/0.070 = 572 lb/in.<br>
<br>

Graves (Die C) and Winkelmann (Die B) tear values increase with increasing hardness. The absolute values obtained with these tests are high, since the test depends to a large extent on tensile factors. For some of the harder compounds, slowing the head speed from 20 inches per minute to one inch per minute permits the sample to relax during the test; this reduces the effect of tensile strength, resulting in lower values for both tests. For example, 50D urethane pulled at 20 inches per minute gives a tear strength which is much greater than that obtained by pulling the same compound at one inch per minute.<br>
<br>

ASTM D-470 and Instron test values increase with increasing hardness for 90A urethane and 95A urethane, but are lower for 50D urethane even though these compounds are harder. Absolute values from these two tests are similar and are the lowest of all the tests studied. Both tests are split tear tests, which tend to eliminate tensile factors and give a more realistic evaluation of cut growth resistance. These tests appear to be the best available measure of the tear resistance of urethane.<br>
<br>

The Trapezoid tear test values increase with hardness, with a large increase for 95A urethane. In the Trapezoid test, mainly used to test rubber coated fabrics, the entire stress is placed at the initiation of tear, in a direction perpendicular to the direction of tear. This eliminates the "doubling back" and shear effects associated with split tears and the tensile effects found in Graves and Winkelmann tests. The Trapezoid test is a useful complement to the other tear tests, but it cannot be used for hard compounds because it is difficult to fold the sample properly.<br>
