<h1>FLATNESS</h1>
Flatness of a surface on a part is the deviation from a true plane or straight edge.<br>
<br>

<img src="images/standfigure7.gif" alt="" class="right">
<h2>Rubber Product (Unground)</h2>
Molded Surfaces (unground) will be flat within 0.25mm (.010in).<br>
<br>

Example:<br>
Fig 7 On a cup as illustrated, the bottom can be concaved or convexed by no more than 0.25mm (.010in).<br>
<br>

<h2>Rubber Product with Metal Insert</h2>
Surfaces that are ground after molding will be flat within 0.12mm (.005 in). (Allowance must be made for removal of stock during grinding operation.)<br>
<br>

<img src="images/standfigure8.gif" alt="" class="right">
Example:<br>
In Sketch 1 Fig 8 after molding, deviation from the true plane can be held to 0.25mm (.010in).<br>
<br>

Example:<br>
In Sketch 2 Fig 8 after grinding, deviation can be held to 0.12mm (.055in) but dimension "H" will necessarily be affected.

