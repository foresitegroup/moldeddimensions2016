<h1>Engineering Project Manager</h1>

<strong>SUMMARY<br>
The Engineering Project Manager will assure that new and revised products meet customer requirements while adhering to Molded Dimensions' safety and profitability objectives. Reports to Vice President of Technical Services.</strong><br>
<br>

<strong>ESSENTIAL DUTIES AND RESPONSIBILITIES</strong><br>
Manages multiple new and revised product projects from receipt of customer purchase order, through material and process development, to customer approval and initial safe launch production runs. Effectively communicates, documents, gathers/analyzes data, anticipates failure modes, problem solves, and delivers on commitments to "Wow internal and external Customers."<br>
<br>

Works closely with other departments, customers and suppliers to "Find a Better Way", implementing continuous improvement projects to reduce manufacturing costs; improve product quality; and/or ensure employee safety.<br>
<br>

Provides direct support to other departments for existing business and special projects. Collaborates on DFM, quality planning, troubleshooting of equipment, molds, raw materials, processes and methods. Communicates customer expectations, revises specifications, conducts experiments, approves alternate processes, etc.<br>
<br>

<strong>QUALIFICATIONS</strong><br>
<ul>
  <li>Five (5) years' experience successfully managing multiple projects in a custom manufacturing environment with direct customer contact.</li>
  <li>Strong verbal and written communication skills</li>
  <li>Experience sourcing and implementing new equipment. Technical proficiency in automated vision inspection systems a plus.</li>
  <li>Proficiency in SolidWorks and MS Excel</li>
  <li>ISO 9001:2015 auditing experience a plus</li>
  <li>Knowledge of: mold construction, metal fabrication, machining processes, inspection and measurement methods including GD&T, AIAG APQP process, statistical techniques, rubber injection molding and urethane casting.</li>
</ul>
<br>

<strong>EDUCATION and/or EXPERIENCE</strong><br>
Bachelor of Science degree in Mechanical Engineering<br>
