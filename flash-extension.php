<h1>FLASH EXTENSION</h1>
<h2>Introduction</h2>
It is the purpose of this section to list and discuss many of the factors that have an effect on the amount of flash, to describe the basic methods by which flash can be removed, and furnish the means by which the designer can designate on the product drawing the flash location and flash variation permissible.<br>
<br>

<h2>Definition</h2>
<ol type="A" style="font-style: italic;">
  <li>Flash is excess rubber on a molded product. It results from cavity overflow and is common to most molding operations. Flash has two dimensions &mdash; extension and thickness.</li>
  <li>Flash extension is the film of rubber projection from the part along the parting line of the mold.</li>
  <li>Flash thickness is measured perpendicular to the mold parting line. Variations in flash thickness are normally included in closure tolerances.</li>
</ol>
<br>

<h2>General Information</h2>
A method for designating permissible flash extension and thickness on a molded product will result in better understanding between rubber manufacturer and consumer and benefit both. This method must permit the designation of a surface where no parting line is permissible. It must also designate areas where a parting line is permissible and define the amount of flash extension tolerable in such areas. The designer, without specific rubber processing knowledge, should be able to specify flash extension limits in any given area on his drawing. Use of RMA Drawing Designations provide in this section will provide this capability; however, the designer should not specify how flash is to be removed. He should specify the amount of flash extension which can be tolerated without impairing product function or appearance. A method designating areas permitting flash and describing flash extension tolerance will result in the following benefits:<br>
<ol type="A" style="font-style: italic;">
  <li>Avoid errors in mold design concerning parting line location.</li>
  <li>Uniformity in appearance and function of molded products supplied by more than one source.</li>
  <li>Simplification of inspection procedures.</li>
  <li>Reduce over-finishing or under-finishing products.</li>
</ol>
<br>

Molding techniques have been developed to produce "flashless" products. The mold parting line, depending on location on the product, is barely discernible with no measurable thickness or extension. Initial cost and maintenance of this tooling and equipment is high and very close manufacturing control is required.<br>
<br>

In instances where flash extension is not a problem or where it is otherwise advantageous, parts are shipped as molded with no flash removal necessary.<br>
<br>

Methods for removing flash from product with metal or other inserts are approximately the same as for non-inserted rubber products. Rubber flash adhering tightly to inserts is generally acceptable. If it must be removed, it is done by mechanical means such as wire brushing, abrasive belts or spat facing. If adhered rubber flash is not permissible, it should be so specified on the drawing.<br>
<br>

Flash removal is an important cost factor in producing finished molded rubber products. Cost conscious designers will permit the widest possible latitude in flash thickness, flash extension, and in location of flash consistent with adequate function and appearance of the product.<br>
<br>

<h1>FACTORS TO BE CONSIDERED IN SETTING STANDARDS ON FLASH</h1>
<h2>Flash Location</h2>
Parting lines (flash lines) must be located to facilitate part removal from the mold cavity after curing.<br>
<br>

<h2>Flash Thickness</h2>
Flash thickness is determined in the molding operation and may vary with mold design, closing pressure, with weight and shape of perform, and type of compound used &mdash; and many lesser factors. Normal variations in flash thickness have been taken into account in the tables set up for closure tolerances, and this will receive no further consideration.<br>
<br>

The designer should be aware that heavy or thick flash is frequently designed to facilitate removal of parts from the mold and to facilitate subsequent handling. In this regard the maximum thickness that can be tolerated without impairing the product function or appearance should be specified.<br>
<br>

<h2>Flash Extension</h2>
There are many methods by which flash extension on rubber products can be removed. The particular method selected will be determined by the degree of flash extension permitted as well as by flash location, flash thickness, and other factors. Some of the more common methods of flash removal are as follows:<br>
<ol type="A" style="font-style: italic;">
  <li>Buffing - A moving abrasive surface material is applied to the rubber part to remove excess rubber by abrasive action.<br><br></li>
  <li>Die Trim - A cutting tool, shaped to the contour of the molded product at the parting line, is applied with a force perpendicular to the flash extension and against either a flat plate or fitted shape. This creates a shearing or pinching action removing the excess flash. Die trim can be done with a hand or machine mounted die. Machine mounted dies are often used for multiple trimming of small uniformly shaped products from multi-cavity molds.<br><br></li>
  <li>Machine Trim - Flash is removed by passing the rubber part through machine mounted rotating or reciprocating cutting tools. These devices are customarily adapted to a particular product and may shear, saw, or skive the flash away.<br><br></li>
  <li>Tumble Trim - There are two basic types of tumble trimming. Both utilize a rotating barrel or drum in which the heavier rubber sections strike the thinner and more fragile flash breaking it free. Dry tumbling at room temperature is most effective with the higher durometer "hard" compounds. The other type of tumbling utilizes carbon dioxide or nitrogen to freeze the molded parts, thus making the compound more brittle so the flash will break more readily. Any tumbling operation will have an effect of surface finish.<br><br></li>
  <li>Mechanical Deflashing - Modern deflashing machines utilize an abrasive medium, tumbling, and a refrigerant for quick freezing. The time and temperature are closely controlled while the parts are agitated in an enclosed barrel. Refrigerant (usually carbon dioxide or nitrogen) is metered into the deflashing chamber while the parts are being impinged with a mechanically agitated abrasive medium. The medium and the tumbling action before the heavier rubber part itself has lost its resiliency. Some loss of surface finish may be expected and some of the abrasive medium may adhere to the molded parts.<br><br></li>
  <li>Pull Trim or Tear Trim - A very thin flash extension is molded immediately adjacent to the part and a thicker flash is molded adjacent to the thin flash but farther from the part. When the flash is pulled from the molded part, it separates at its thinnest point adjacent to the molded part. This method may result in a sawtooth or irregular appearance and it is limited to certain compounds and product designs.<br><br></li>
  <li>Hand Trim Flash is removed by an expedient method using hand tools such as knives, scissors, razor blades or skiving knives.<br><br></li>
</ol>

<h2>Method of Designation of Flash</h2>
<strong>Extension</strong> The symbol "T" with a notation in hundredths of a millimeter for the maximum extension shall be used. Example: T .80mm. (.80mm maximum extension permitted.) IF ENGLISH DIMENSION THE DRAWING DESIGNATION WILL NOT BE FOLLWED BY ANY LETTERS. EXAMPLE T .032.<br>
<br>

<strong>Thickness</strong> The flash thickness may be specified following the extension limit if it is critical to the function of the part. Closure tolerances will apply as in table 2, 3, 4, and 5 on page 6.<br>
<br>

<strong>Location</strong> An arc enclosing the actual area included by this designation and a leader to the trim symbol designates the maximum allowable flash extension and thickness thus enclosed. If no flash can be tolerated in a given area, the symbol "T" .00mm is used.<br>
