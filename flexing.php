<h1>Flexing</h1>
When subjected to flexing, rubber products frequently fail due to the development and propagation of cracks. The cracks reduce other properties, which in turn reduce the service life of the rubber. Cracks can grow through mechanical means or by oxidative and ozone attack.<br>
<br>

ASTM D-430, Method B, is a test designed to produce cracking by bending. The time or number of flexes to crack initiation are used as the measure of performance. It employs a DeMattia flexing machine which flexes a 6" x 1" x Â¼" specimen having a 0.094" round groove molded transversely in the center of the strip. This machine operates at 300 cycles per minute.<br>
