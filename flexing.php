Softer vulcanizates like urethane have excellent flex life. In the Ross notched test, no cut growth occurred during 420,000 flexes (70 hrs) at a rate of 100 cycles/minute. The more vigorous DeMattia test, run at 300 cycles/minute, caused failure in 24 hours using notched specimens; but unnotched samples ran for 100 hours (1,800,000 flexes) with only slight cracking occurring.<br>
<br>

Design of the part to reduce localized concentration of the stress or heat build-up will improve flex life. When an elastomeric part is flexed, very high stresses are developed in thick cross sections. Under repeated flexing, any cut in the surface of the part will grow larger because of the high local stresses concentrated at the cut. As with any elastomer, the rate of cut growth under flexing may be reduced (Figure 5) by decreasing the thickness of the part.<br>
